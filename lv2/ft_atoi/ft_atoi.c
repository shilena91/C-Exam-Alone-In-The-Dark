int	ft_atoi(const char *str)
{
	int	i;
	int	p;
	int nb;

	i = 0;
	nb = 0;
	p = 1;
	while (str[i] <= 32)
		i++;
	if (str[i] == '-')
		p = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	return (nb * p);
}
